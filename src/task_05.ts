//array methods
let number_task05 : number[] = [20, 30, 40];

console.log(number_task05);

// insert the number at the end of the array
number_task05.push(50);
// insert the number at the start of the array
number_task05.unshift(10);

console.log(number_task05);

// remove the number at the end of the array
number_task05.pop();
//remove the number at the start of the array
number_task05.shift();

console.log(number_task05);